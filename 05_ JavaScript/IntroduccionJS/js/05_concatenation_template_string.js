const my_name = "Joshua",
	mail = "mail@mail.com";

// Concatenation
console.log("Concatenación con signo + -> Nombre del cliente: " + my_name + " - Correo: " + mail);
console.log("Concatenación con coma , -> Nombre del cliente: " , my_name , " - Correo: " , mail);

// Template String - String Literals
console.log(`Template string o string literals -> Nombre del cliente: ${my_name} - Correo: ${mail}`);